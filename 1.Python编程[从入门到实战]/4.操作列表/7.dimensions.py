# 元组
dimensions = (200, 50)
print(dimensions[0])  # 200
print(dimensions[1])  # 50

# 给元组重新赋值
print("Original dimensions:")
for dimension in dimensions:
    print(dimension)
'''
    Original dimensions:
    200
    50
'''

dimensions = (400, 100)
print("\nModified dimensions:")
for dimension in dimensions:
    print(dimension)
'''
    Modified dimensions:
    400
    100
'''
