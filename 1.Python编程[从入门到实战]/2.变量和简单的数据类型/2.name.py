# 合并字符串
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name

# 修改字符串大小写
message = "Hello, " + full_name.title() + "!"
print(message)
# Hello, Ada Lovelace!

# 添加空白
print('python')
# python

print('\tpython')
#	python

# 删除空白
language = 'python '
print(language.rstrip())
# python
